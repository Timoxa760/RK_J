func (d *DictCategorizer) Categorize(items []internal.Item) []internal.CategorizedItem {
	result := make([]internal.CategorizedItem, 0, len(items))
	for _, item := range items {
		name := strings.ToLower(strings.TrimSpace(item.Name))
		cat := d.findCategory(name)
		result = append(result, internal.CategorizedItem{
			Name:        item.Name,
			Price:       item.Price,
			Quantity:    item.Quantity,
			Category:    cat,
			IsImpulsive: d.impulsive[name],
		})
	}
	return result
}

func (d *DictCategorizer) findCategory(name string) string {
	if c, ok := d.categories[name]; ok {
		return c
	}
	for prefix, c := range d.categories {
		if strings.Contains(name, prefix) {
			return c
		}
	}
	return "Прочее"
}
